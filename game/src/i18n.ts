const LOCALES: Record<Lang, Dict> = { en, ru, uz };

let current: Lang = "en";
const listeners = new Set<() => void>();

export function setLang(l: Lang): void {
  current = l;
  try { localStorage.setItem("mys.lang", l); } catch { /* ignore */ }
  listeners.forEach((fn) => fn());
}
export function getLang(): Lang { return current; }
export function onLangChange(fn: () => void): void { listeners.add(fn); }

export function initLang(): void {
  try {
    const saved = localStorage.getItem("mys.lang") as Lang | null;
    if (saved && LOCALES[saved]) current = saved;
  } catch { /* ignore */ }
}

export function t(key: string, params?: Record<string, string | number>): string {
  let s = LOCALES[current][key] ?? LOCALES.en[key] ?? key;
  if (params) {
    for (const k of Object.keys(params)) s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(params[k]));
  }
  return s;
}

// CI-style parity check (used by the headless test).
export function localeParity(): string[] {
  const keys = new Set<string>([...Object.keys(en), ...Object.keys(ru), ...Object.keys(uz)]);
  const missing: string[] = [];
  for (const k of keys) {
    if (!(k in en)) missing.push(`en:${k}`);
    if (!(k in ru)) missing.push(`ru:${k}`);
    if (!(k in uz)) missing.push(`uz:${k}`);
  }
  return missing;
}

// Player display name, persisted to localStorage("mys.name"). defaultName() is read everywhere a
// name is needed (lobby, hello); setDefaultName() is the writer the editable name field uses so the
// chosen name survives across sessions (spec §24 T33-D1). Both guard against a missing localStorage
// (headless test / privacy mode) so they are safe to call anywhere.
export function defaultName(): string {
  try { return localStorage.getItem("mys.name") || "Player"; } catch { return "Player"; }
}
export function setDefaultName(name: string): void {
  try { localStorage.setItem("mys.name", name); } catch { /* ignore */ }
}
